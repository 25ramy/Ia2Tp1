class node:

    def __init__(self,pos,gn,hn):
        self.__pos = pos
        self.__gn = gn
        self.__hn = hn
        
    def get_gn(self):
        return self.__gn

    def set_gn(self, gn):
        self.__gn = gn

        
    def get_hn(self):
        return self.__hn

    def set_hn(self, hn):
        self.__hn = hn

        
    def get_pos(self):
        return self.__pos

    def set_pos(self, pos):
        self.__pos = pos




        